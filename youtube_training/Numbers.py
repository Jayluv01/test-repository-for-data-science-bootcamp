# There are three types of numbers: 
# 1. Integers (x = 1)
# 2. Floats (x = 1.1)
# 3. Complex numbers (1 + 2j)

# The standard arithmetic operators
print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3) # This produces a 'float answer' If we want an integer answer, we use (//)
print(10 // 3) # This produces a 'float answer' If we want an integer answer, we use (//)
print(10 ** 3) # Raise to power (10 raised to the power of 3)
print(10 % 3) 

# Useful funnctions to work with NUMBERS

print(round (2.9)) # ROUND function is used to round off a float
print(abs(-2.9))

